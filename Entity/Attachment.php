namespace TechG\AppFw\BaseBundle\Entity;

use Doctrine\ORM\Mapping as ORM;

use TechG\AppFw\BaseBundle\Extensions\BaseEntity as BaseEntity;

/**
 * Attachment
 *
 * @ORM\Table(name="attachment")
 * @ORM\Entity
 */
class Attachment extends BaseEntity
{
    /**
     * @var integer
     *
     * @ORM\Column(name="id", type="bigint", nullable=false)
     * @ORM\Id
     * @ORM\GeneratedValue(strategy="IDENTITY")
     */
    private $id;

    /**
     * @var string
     *
     * @ORM\Column(name="ori_filename", type="string", length=255, nullable=true)
     */
    private $oriFilename;

    /**
     * @var string
     *
     * @ORM\Column(name="filename", type="string", length=255, nullable=false)
     */
    private $filename;

    /**
     * @var string
     *
     * @ORM\Column(name="ext", type="string", length=5, nullable=false)
     */
    private $ext;

    /**
     * @var integer
     *
     * @ORM\Column(name="size", type="integer", nullable=true)
     */
    private $size;

    /**
     * @var string
     *
     * @ORM\Column(name="mime", type="string", length=50, nullable=true)
     */
    private $mime;

    /**
     * @var boolean
     *
     * @ORM\Column(name="enabled", type="boolean", nullable=false)
     */
    private $enabled = false;

    /**
     * @var \Category
     *
     * @ORM\ManyToOne(targetEntity="Category")
     * @ORM\JoinColumns({
     *   @ORM\JoinColumn(name="category_id", referencedColumnName="id")
     * })
     */
    private $category;

    /**
     * @var \AttachmentType
     *
     * @ORM\ManyToOne(targetEntity="AttachmentType")
     * @ORM\JoinColumns({
     *   @ORM\JoinColumn(name="attachment_type_id", referencedColumnName="id")
     * })
     */
    private $attachmentType;


    public function __construct()
    {
        parent::__construct();
    }       

}
